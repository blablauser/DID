class Validate

  def self.capitalize_each string
    string = string.split(' ').map {|w| w.capitalize }.join(' ')
    return string
  end

  def self.check_for_hex_code string
    string.gsub!("&#x2019;", "'")
    string.gsub!("&#x2013;", "-")
    string.gsub!("&#x2018;", "'")
    string.gsub!("&#x2026;", "...")
    string.gsub!("&#x201C;", "\"")
    string.gsub!("&#x201D;", "\"")
    string.gsub!("&#xE0;", "a")
