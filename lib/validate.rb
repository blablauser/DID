    string.gsub!("&#xEB;", "e")
    string.gsub!("&#xED;", "i")
    string.gsub!("&#xEE;", "i")
    string.gsub!("&#xEF;", "i")
    string.gsub!("&#xC0;", "A")
    string.gsub!("&#xC1;", "A")
    string.gsub!("&#xC9;", "E")
    string.gsub!("&#xC8;", "E")
    string.gsub!("&#xCA;", "E")
    string.gsub!("&#xD3;", "O")
    string.gsub!("&#xF1;", "n")
    string.gsub!("&#xF2;", "o")
    string.gsub!("&#xF3;", "o")
    string.gsub!("&#xF6;", "o")
    string.gsub!("&#xF8;", "o")
